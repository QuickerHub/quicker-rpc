/**
 * Fetches vscode-icons from Iconify and writes lib/file-type-icon-svgs.generated.ts
 * Run: node scripts/build-file-type-icons.mjs
 */
import { writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const KIND_TO_ICON = {
  json: "file-type-json",
  jsonc: "file-type-json",
  csharp: "file-type-csharp",
  javascript: "file-type-js",
  typescript: "file-type-typescript",
  tsx: "file-type-reactts",
  jsx: "file-type-reactjs",
  python: "file-type-python",
  markdown: "file-type-markdown",
  html: "file-type-html",
  css: "file-type-css",
  scss: "file-type-scss",
  less: "file-type-less",
  xml: "file-type-xml",
  svg: "file-type-svg",
  yaml: "file-type-yaml",
  shell: "file-type-shell",
  powershell: "file-type-powershell",
  rust: "file-type-rust",
  go: "file-type-go",
  java: "file-type-java",
  kotlin: "file-type-kotlin",
  ruby: "file-type-ruby",
  php: "file-type-php",
  sql: "file-type-sql",
  docker: "file-type-docker",
  toml: "file-type-toml",
  ini: "file-type-ini",
  env: "file-type-dotenv",
  config: "file-type-config",
  git: "file-type-git",
  proto: "file-type-protobuf",
  text: "file-type-text",
  generic: "file-type-text",
};

const uniqueIcons = [...new Set(Object.values(KIND_TO_ICON))];
const iconSvg = new Map();

for (const icon of uniqueIcons) {
  const url = `https://api.iconify.design/vscode-icons/${icon}.svg`;
  const res = await fetch(url);
  if (!res.ok) throw new Error(`Failed ${icon}: ${res.status}`);
  let svg = await res.text();
  svg = svg
    .replace(/width="[^"]*"/, 'width="16"')
    .replace(/height="[^"]*"/, 'height="16"');
  iconSvg.set(icon, svg);
  console.log("ok", icon);
}

const lines = [
  "// Generated by scripts/build-file-type-icons.mjs — vscode-icons (Iconify)",
  "// License: MIT (vscode-icons)",
  "",
  'import type { FileIconKind } from "./file-icon-kind";',
  "",
  "const ICON_SVG: Record<string, string> = {",
];
for (const [icon, svg] of iconSvg) {
  const escaped = svg.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$/g, "\\$");
  lines.push(`  "${icon}": \`${escaped}\`,`);
}
lines.push("};");
lines.push("");
lines.push("const KIND_TO_ICON: Record<FileIconKind, string> = {");
for (const [kind, icon] of Object.entries(KIND_TO_ICON)) {
  lines.push(`  ${kind}: "${icon}",`);
}
lines.push("};");
lines.push("");
lines.push("export function getFileTypeIconSvg(kind: FileIconKind): string {");
lines.push("  const key = KIND_TO_ICON[kind] ?? KIND_TO_ICON.generic;");
lines.push("  return ICON_SVG[key] ?? ICON_SVG[KIND_TO_ICON.generic];");
lines.push("}");

const outPath = join(dirname(fileURLToPath(import.meta.url)), "../lib/file-type-icon-svgs.generated.ts");
writeFileSync(outPath, lines.join("\n"), "utf8");
console.log("wrote", outPath);
